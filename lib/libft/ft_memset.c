#include "libft.h"

void				*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*str;
	size_t			i;

	i = 0;
	str = (unsigned char *)b;
	while (i < len)
		*(str + i++) = (unsigned char)c;
	return (b);
}
